#include <stdio.h>
/**
 * main - A program that Print all the letters except q and e
 *
 * Return: 0
*/
int main(void)
{
	char c;
	char x;
	char y;

	c = 'a';
	x = 'e';
	y = 'q';
	while (c <= 'z')
	{
		putchar(c);
		c++;
	if
		(c == y && c == x);
	{
		putchar(c++);
	}
	}
	putchar('\n');
	return (0);
}
